#include <bits/stdc++.h>

using ll = long long int;

int main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(0);

    int t = 0;
    std::cin >> t;

    while (t--) {
        ll n = 0;
        ll x = 0;
        std::cin >> n >> x;
        std::cout << (x << 1) << std::endl;
    }

    return 0;
}
